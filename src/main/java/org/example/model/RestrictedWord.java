package org.example.model;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * Модель для представления запрещённого слова.
 * 
 * Класс предназначен для хранения информации о словах, которые не допускаются в системе.
 * Содержит два поля: ключ (само слово) и значение (дополнительные данные о слове).
 * 
 * Аннотации Lombok используются для автоматической генерации методов, таких как
 * геттеры, сеттеры, конструкторы, equals, hashCode и toString.
 */
@Data
@NoArgsConstructor
@AllArgsConstructor
public class RestrictedWord {

    /**
     * Уникальный идентификатор слова.
     * 
     * Поле представляет собой слово, которое приводится к нижнему регистру для обеспечения консистентности.
     * Используется как ключ для идентификации запрещённого слова в системе.
     */
    private String key;

    /**
     * Дополнительная информация о слове.
     * 
     * Поле может содержать описание причины запрета слова, контекст использования или
     * любые другие метаданные, связанные с этим словом.
     */
    private String value;
}
